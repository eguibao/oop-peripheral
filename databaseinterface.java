    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // Find button action for jTextField7

        try {
            // Get the value from jTextField7
            String searchValue = jTextField7.getText();

            // Check if the value is not empty
            if (!searchValue.isEmpty()) {
                // Create the select query
                String selectQuery = "SELECT * FROM students WHERE ID = ?";
                PreparedStatement selectPst = conn.prepareStatement(selectQuery);

                // Set the ID value in the prepared statement
                selectPst.setString(1, searchValue);

                // Execute the select query
                ResultSet rs = selectPst.executeQuery();

                // Check if a record is found
                if (rs.next()) {
                    

                    // Prompt the user for updated values
                    String peripheral = JOptionPane.showInputDialog("Enter new peripheral");
                    String employee = JOptionPane.showInputDialog("Enter new employee");
                    String type = JOptionPane.showInputDialog("Enter new type");

                    // Create the update query
                    String updateQuery = "UPDATE students SET peripheral_name = ?, employee = ?, type = ? WHERE ID = ?";
                    PreparedStatement updatePst = conn.prepareStatement(updateQuery);

                    // Set the updated values in the prepared statement
                    updatePst.setString(1, peripheral);
                    updatePst.setString(2, employee);
                    updatePst.setString(3, type);
                    updatePst.setString(4, searchValue);

                    // Execute the update query
                    int affectedRows = updatePst.executeUpdate();

                    if (affectedRows > 0) {
                        JOptionPane.showMessageDialog(null, "Record updated successfully");
                    } else {
                        JOptionPane.showMessageDialog(null, "Failed to update record");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Record not found");
                    
                }
            } else {
                JOptionPane.showMessageDialog(null, "Please enter an ID to search");
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

