    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // Insert button action for jTextField2, jTextField3, jTextField4, jTextField5

        try {
            String sqlquery = "INSERT INTO students (ID, peripheral_name, employee, type) VALUES (?, ?, ?,?)";
            PreparedStatement pst = conn.prepareStatement(sqlquery);

            // Get values from text fields
            String id = jTextField2.getText();
            String peripheral = jTextField3.getText();
            String employee = jTextField4.getText();
            String type= jTextField5.getText();

            // Set values in the prepared statement
            pst.setString(1, id);
            pst.setString(2, peripheral);
            pst.setString(3, employee);
            pst.setString(4, type);

            // Execute the insert query
            int affectedRows = pst.executeUpdate();

            if (affectedRows > 0) {
                JOptionPane.showMessageDialog(null, "Record added successfully");
            } else {
                JOptionPane.showMessageDialog(null, "Failed to add record");
            }

            // Clear text fields after insertion
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // Delete button action for jTextField6

        try {
            // Get the value from jTextField6
            String idToDelete = jTextField6.getText();

            // Check if the value is not empty
            if (!idToDelete.isEmpty()) {
                // Convert the value to an integer (assuming ID is an integer)
                int id = Integer.parseInt(idToDelete);

                // Create the delete query
                String sqlquery = "DELETE FROM students WHERE ID = ?";
                PreparedStatement pst = conn.prepareStatement(sqlquery);

                // Set the ID value in the prepared statement
                pst.setInt(1, id);

                // Execute the delete query
                int affectedRows = pst.executeUpdate();

                if (affectedRows > 0) {
                    JOptionPane.showMessageDialog(null, "Record deleted successfully");
                } else {
                    JOptionPane.showMessageDialog(null, "Record not found or deletion failed");
                }

                // Clear jTextField6 after deletion
                jTextField6.setText("");
            } else {
                JOptionPane.showMessageDialog(null, "Please enter an ID to delete");
            }

        } catch (NumberFormatException | SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

