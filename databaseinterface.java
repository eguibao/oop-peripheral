    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    Connection conn = Projectdatabase.conn();
PreparedStatement pst;
ResultSet rs;
try {
    String sqlquery = "SELECT * FROM students";
    pst = conn.prepareStatement(sqlquery);
    rs = pst.executeQuery();

    StringBuilder allData = new StringBuilder();

    while (rs.next()) {
        int id = rs.getInt(1);
        String name = rs.getString(2);
        String employee = rs.getString(3);
        String type = rs.getString(4);

        allData.append("\n").append(id).append("  ").append(name).append("  ").append(employee).append("  ").append(type).append(" | ");
    }

    // Set the concatenated data in the jTextField1
    jTextField1.setText(allData.toString());

} catch (SQLException e) {
    JOptionPane.showMessageDialog(null, e);
}
    }//GEN-LAST:event_jButton2ActionPerformed

